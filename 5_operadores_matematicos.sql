--Operador + 
SELECT 1 + 3 as resultado;

--Operador * 
SELECT 3 * 2  as resultado;

--Operador - 
SELECT 5 - 2 as resultado;

--Operador / 
SELECT 15 / 3 as resultado
 

--Operador %  Mod
--mostra o restando da divisao
SELECT 12%5 as resultado;
--sobra zero
SELECT 12%4 as resultado

-- Equação
select ((1+4)*(3*3))/5 as resultado;


